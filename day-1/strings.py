# Strings are sequences of characters used to store and manipulate text in Python.
# They are enclosed in single quotes (' '), double quotes (" "), or triple quotes (''' ''' or """ """).

print('100 + 50')
print('100' + '50')  # Concatenation 
print('Hello, ' + 'Fede')  

# Escape Characters
print('My name is \'Fede\'')  # Using backslash to escape single quote
print("First line\nSecond line")  # New line
print('A\tB\tC\nD\tE\tF\nG\tH\tI')
print("\tColumn1\tColumn2")  # Tab space
print('This sign \\ is a backslash') # Printing a backslash
print('Backslash (\\)')
print("Forward Slash (/)")